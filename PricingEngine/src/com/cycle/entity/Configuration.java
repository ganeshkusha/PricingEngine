package com.cycle.entity;

/*
 * This pojo provides configurations of every component class
 */

public class Configuration {

	private String frameConfig;
	private String handleBarWithBrakesConfig;
	private String seatingConfig;
	private String wheelsConfig;
	private String chainAssemblyConfig;

	public String getFrameConfig() {
		return frameConfig;
	}

	public void setFrameConfig(String frameConfig) {
		this.frameConfig = frameConfig;
	}

	public String getHandleBarWithBrakesConfig() {
		return handleBarWithBrakesConfig;
	}

	public void setHandleBarWithBrakesConfig(String handleBarWithBrakesConfig) {
		this.handleBarWithBrakesConfig = handleBarWithBrakesConfig;
	}

	public String getSeatingConfig() {
		return seatingConfig;
	}

	public void setSeatingConfig(String seatingConfig) {
		this.seatingConfig = seatingConfig;
	}

	public String getWheelsConfig() {
		return wheelsConfig;
	}

	public void setWheelsConfig(String wheelsConfig) {
		this.wheelsConfig = wheelsConfig;
	}

	public String getChainAssemblyConfig() {
		return chainAssemblyConfig;
	}

	public void setChainAssemblyConfig(String chainAssemblyConfig) {
		this.chainAssemblyConfig = chainAssemblyConfig;
	}

}
